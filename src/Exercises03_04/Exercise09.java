package Exercises03_04;
// Check the calculations: 'a' + 3 = and 'd' - 3 =.

public class Exercise09 {
 public static void main(String[] args) {
  // 'a' and 'c' are character type literals and can be treated as integers
  System.out.println('a' + 3); // 97 + 3
  System.out.println('d' - 3); // 100 - 3
  System.out.printf("%c \n", 'a' + 3);
  System.out.printf("%c \n", 'd' - 3);
 }
}
