package Lecture05;
// No break in the switch statement

public class Example09 {
 public static void main ( String[] args ) {
  int a = 10;
  switch (a) {
  case 1:
   System.out.println("a = 1");
  case 10:
   System.out.println("a = 10");
  default:
   System.out.println("a is not 1 and 10");
  }
 }
}
